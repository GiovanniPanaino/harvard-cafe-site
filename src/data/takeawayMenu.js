function randToCents(value) {
  return Math.round(Number(value) * 100)
}

function item(categoryId, slug, name, description, price, options = {}) {
  const priceCents = randToCents(price)
  return {
    id: `${categoryId}-${slug}`,
    category_id: categoryId,
    name,
    description,
    price_cents: priceCents,
    priceCents,
    available: true,
    ...options,
  }
}

